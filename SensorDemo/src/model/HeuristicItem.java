package model;

/**
 *
 * @author sev_user
 */
public class HeuristicItem {
    int id;
    float value;

    public HeuristicItem() {
    }

    public HeuristicItem(int id, float value) {
        this.id = id;
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }
    
}
