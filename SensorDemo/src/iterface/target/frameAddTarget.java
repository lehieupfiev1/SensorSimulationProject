    private void AddNewTargetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddNewTargetBtnActionPerformed
        // TODO add your handling code here:
        if (manualPositionXTextField.getText().equals("") || manualPositionYTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please insert Valid Number Only");
        } else if (positionX >= SensorUtility.numberRow || positionY >= SensorUtility.numberColum){
            int maxX = SensorUtility.numberRow-1;
            int maxY = SensorUtility.numberColum-1;
            JOptionPane.showMessageDialog(null, "Maximum positon X =" + maxX +" and Maximum positon Y =" +maxY);
        } else {
            SensorUtility.mListTargetNodes.add(new NodeItem(positionX, positionY, 0));
            updateNumberTarget();
        }
    }//GEN-LAST:event_AddNewTargetBtnActionPerformed

    private void RandomTargetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RandomTargetButtonActionPerformed
        // TODO add your handling code here:
        if (RandomNumberTargetTextField.getText().equals("") ) {
            JOptionPane.showMessageDialog(null, "Please insert Valid Number Only");
        } else {
            Random random = new Random();
            for (int i = 0;i < numberTarget;i++) {
               int x = random.nextInt(SensorUtility.numberRow-1);
               int y = random.nextInt(SensorUtility.numberColum-1);
               SensorUtility.mListTargetNodes.add(new NodeItem(x, y, 0));
            }
            JOptionPane.showMessageDialog(null, "Create new targets success !");
            updateNumberTarget();
        }
    }//GEN-LAST:event_RandomTargetButtonActionPerformed

